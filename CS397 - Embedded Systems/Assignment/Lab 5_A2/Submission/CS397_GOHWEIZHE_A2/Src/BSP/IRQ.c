/*****************************************************************************
 Define
******************************************************************************/


/*****************************************************************************
 Type definition
******************************************************************************/
extern void TIMER0A_IRQHandler( uint32_t Status );

/*****************************************************************************
 Global Variables
******************************************************************************/
extern void GPIOF_Button_IRQHandler( uint32_t Status );

/*****************************************************************************
 Local Variables
******************************************************************************/
/** Enable interrupt for Timer 0 (in IRQ.c) **/
void IRQ_Init( void )
{
	NVIC_EnableIRQ( TIMER0A_IRQn );
	NVIC_SetPriority (GPIOF_IRQn, 5);
	NVIC_EnableIRQ( GPIOF_IRQn);
}
void TIMER0A_Handler( void )
{
	uint32_t status = TIMER0->RIS; // or MIS register
	TIMER0A_IRQHandler (status );
}



/*****************************************************************************
 Implementation
******************************************************************************/
void  GPIOF_Handler( void )
{
	uint32_t status = GPIOF->RIS;
	GPIOF_Button_IRQHandler (status );
}

